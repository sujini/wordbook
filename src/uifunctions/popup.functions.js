export const ShowSignIn = () =>{
    document.getElementById('sign-in').classList.add('show');
};
export const ShowSignUp = () =>{
    document.getElementById('sign-up').classList.add('show');
};

export const HideSignIn = () =>{
    document.getElementById('sign-in').classList.remove('show');
};
export const HideSignUp = () =>{
    document.getElementById('sign-up').classList.remove('show');
};